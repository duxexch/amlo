// Due to file size limits, I'll create a helper function that generates the full translation maps inline

// Helper: set nested key
function setNestedKey(obj, dotPath, value) {
  const parts = dotPath.split('.');
  let curr = obj;
  for (let i = 0; i < parts.length - 1; i++) {
    if (!curr[parts[i]]) curr[parts[i]] = {};
    curr = curr[parts[i]];
  }
  curr[parts[parts.length - 1]] = value;
}

// Helper: get nested key
function getNestedKey(obj, dotPath) {
  const parts = dotPath.split('.');
  let curr = obj;
  for (const p of parts) {
    if (curr === undefined || curr === null) return undefined;
    curr = curr[p];
  }
  return curr;
}

// Helper: get all leaf keys
function getKeys(obj, prefix = '') {
  let keys = [];
  for (const k of Object.keys(obj)) {
    const full = prefix ? `${prefix}.${k}` : k;
    if (typeof obj[k] === 'object' && obj[k] !== null) {
      keys = keys.concat(getKeys(obj[k], full));
    } else {
      keys.push(full);
    }
  }
  return keys;
}

const arKeys = getKeys(ar);

// Process each non-en locale
const locales = ['de', 'es', 'fa', 'fr', 'hi', 'id', 'ja', 'ko', 'pt', 'ru', 'tr', 'ur', 'zh'];

for (const lang of locales) {
  const filePath = path.join(localesDir, `${lang}.json`);
  const existing = JSON.parse(fs.readFileSync(filePath, 'utf8'));
  const existingKeys = new Set(getKeys(existing));
  const missingKeys = arKeys.filter(k => !existingKeys.has(k));
  
  if (missingKeys.length === 0) {
    console.log(`${lang}: Already complete ✓`);
    continue;
  }

  const langTranslations = translations[lang];
  
  for (const key of missingKeys) {
    // Check if we have a manual translation
    if (langTranslations && langTranslations[key]) {
      setNestedKey(existing, key, langTranslations[key]);
    } else {
      // Fallback to English value
      const enVal = getNestedKey(en, key);
      if (enVal !== undefined) {
        setNestedKey(existing, key, enVal);
      } else {
        // Last fallback: Arabic value
        const arVal = getNestedKey(ar, key);
        setNestedKey(existing, key, arVal);
      }
    }
  }

  fs.writeFileSync(filePath, JSON.stringify(existing, null, 2) + '\n', 'utf8');
  console.log(`${lang}: Added ${missingKeys.length} missing keys ✓`);
}

