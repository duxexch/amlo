// Apply
function applyTranslations(langCode, translationMap) {
  const filePath = path.join(dir, `${langCode}.json`);
  const data = JSON.parse(fs.readFileSync(filePath, 'utf8'));
  let count = 0;
  for (const [dotKey, value] of Object.entries(translationMap)) {
    const current = getVal(data, dotKey);
    const enValue = getVal(en, dotKey);
    if (current === enValue || current === undefined) {
      setVal(data, dotKey, value);
      count++;
    }
  }
  fs.writeFileSync(filePath, JSON.stringify(data, null, 2) + '\n', 'utf8');
  console.log(`${langCode}: Updated ${count} translations`);
}

